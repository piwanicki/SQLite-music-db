package sample.model;

public class ArtistsSongs {

    private String artistName;
    private String albumsName;
    private String songTrack;

    public String getSongTitle() {
        return songTitle;
    }

    public void setSongTitle(String songTitle) {
        this.songTitle = songTitle;
    }

    private String songTitle;

    public String getArtistName() {
        return artistName;
    }

    public void setArtistName(String artistName) {
        this.artistName = artistName;
    }

    public String getAlbumsName() {
        return albumsName;
    }

    public void setAlbumsName(String albumsName) {
        this.albumsName = albumsName;
    }

    public String getSongTrack() {
        return songTrack;
    }

    public void setSongTrack(String songTrack) {
        this.songTrack = songTrack;
    }
}
